package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        int c = 99;
        int h = 104;
        int a = 97;
        int r = 114;

        char c1 = (char) c;
        char h1 = (char) h;
        char a1 = (char) a;
        char r1 = (char) r;

        System.out.println(c1);
        System.out.println(h1);
        System.out.println(a1);
        System.out.println(r1);
    }
}
